#include "fly/task/loop_executor.hpp"

namespace fly {
namespace task {

void Loop_Executor::run()
{
    while(m_running.load(std::memory_order_relaxed))
    {
        run_in_loop();
    }
}

void Loop_Executor::stop()
{
    m_running.store(false, std::memory_order_relaxed);
}

}
}
